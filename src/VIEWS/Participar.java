    private void btnRegistarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistarActionPerformed
        // TODO add your handling code here:

        try {

            Connection con = ConexaBD.ligar();
            String inserir = "INSERT INTO partcipante(Nome, Sobrenome, Categoria, )"
            + " values (?,?,?)";
            PreparedStatement ps = con.prepareStatement(inserir);
            ps.setString(1, txtNomp.getText());
            ps.setString(2, txtSobrenomeP.getText());
            ps.setString(3, txtCategoriaP.getText());
            ps.execute();
            ps.close();
            con.close();

            JOptionPane.showMessageDialog(null, "PARTIPANTE REGISTADO");
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, erro);
        }
    }//GEN-LAST:event_btnRegistarActionPerformed

